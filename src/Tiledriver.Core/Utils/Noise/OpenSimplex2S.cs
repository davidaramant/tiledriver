using System.Runtime.CompilerServices;

namespace Tiledriver.Core.Utils.Noise;

internal static class OpenSimplex2S
{
	private const long PrimeX = 0x5205402B9270C86FL;
	private const long PrimeY = 0x598CD327003817B5L;
	private const long PrimeZ = 0x5BCC226E9FA0BACBL;
	private const long PrimeW = 0x56CC5227E58F554BL;
	private const long HashMultiplier = 0x53A3F72DEEC546F5L;
	private const long SeedFlip3D = -0x52D547B2E96ED629L;

	private const double Root2Over2 = 0.7071067811865476;
	private const double Skew2D = 0.366025403784439;
	private const double Unskew2D = -0.21132486540518713;

	private const double Root3Over3 = 0.577350269189626;
	private const double FallbackRotate3 = 2.0 / 3.0;
	private const double Rotate3Orthogonalizer = Unskew2D;

	private const float Skew4D = 0.309016994374947f;
	private const float Unskew4D = -0.138196601125011f;

	private const int NGrads2DExponent = 7;
	private const int NGrads3DExponent = 8;
	private const int NGrads4DExponent = 9;
	private const int NGrads2D = 1 << NGrads2DExponent;
	private const int NGrads3D = 1 << NGrads3DExponent;
	private const int NGrads4D = 1 << NGrads4DExponent;

	private const double Normalizer2D = 0.05481866495625118;
	private const double Normalizer3D = 0.2781926117527186;
	private const double Normalizer4D = 0.11127401889945551;

	private const float Rsquared2D = 2.0f / 3.0f;
	private const float Rsquared3D = 3.0f / 4.0f;
	private const float Rsquared4D = 4.0f / 5.0f;

	#region Noise Evaluators

	/// <summary>
	/// 2D OpenSimplex2S/SuperSimplex noise, standard lattice orientation.
	/// </summary>
	public static float Noise2(long seed, double x, double y)
	{
		// Get points for A2* lattice
		double s = Skew2D * (x + y);
		double xs = x + s,
			ys = y + s;

		return Noise2UnskewedBase(seed, xs, ys);
	}

	/// <summary>
	/// 2D OpenSimplex2S/SuperSimplex noise, with Y pointing down the main diagonal.
	/// </summary>
	/// <remarks>
	/// Might be better for a 2D sandbox style game, where Y is vertical.
	/// Probably slightly less optimal for heightmaps or continent maps,
	/// unless your map is centered around an equator. It's a slight
	/// difference, but the option is here to make it easy.
	/// </remarks>
	public static float Noise2ImproveX(long seed, double x, double y)
	{
		// Skew transform and rotation baked into one.
		double xx = x * Root2Over2;
		double yy = y * (Root2Over2 * (1 + 2 * Skew2D));

		return Noise2UnskewedBase(seed, yy + xx, yy - xx);
	}

	/// <summary>
	/// 2D OpenSimplex2S/SuperSimplex noise base.
	/// </summary>
	private static float Noise2UnskewedBase(long seed, double xs, double ys)
	{
		// Get base points and offsets.
		int xsb = FastFloor(xs),
			ysb = FastFloor(ys);
		float xi = (float)(xs - xsb),
			yi = (float)(ys - ysb);

		// Prime pre-multiplication for hash.
		long xsbp = xsb * PrimeX,
			ysbp = ysb * PrimeY;

		// Unskew.
		float t = (xi + yi) * (float)Unskew2D;
		float dx0 = xi + t,
			dy0 = yi + t;

		// First vertex.
		float a0 = Rsquared2D - dx0 * dx0 - dy0 * dy0;
		float value = (a0 * a0) * (a0 * a0) * Grad(seed, xsbp, ysbp, dx0, dy0);

		// Second vertex.
		float a1 =
			(float)(2 * (1 + 2 * Unskew2D) * (1 / Unskew2D + 2)) * t
			+ ((float)(-2 * (1 + 2 * Unskew2D) * (1 + 2 * Unskew2D)) + a0);
		float dx1 = dx0 - (float)(1 + 2 * Unskew2D);
		float dy1 = dy0 - (float)(1 + 2 * Unskew2D);
		value += (a1 * a1) * (a1 * a1) * Grad(seed, xsbp + PrimeX, ysbp + PrimeY, dx1, dy1);

		// Third and fourth vertices.
		// Nested conditionals were faster than compact bit logic/arithmetic.
		float xmyi = xi - yi;
		if (t < Unskew2D)
		{
			if (xi + xmyi > 1)
			{
				float dx2 = dx0 - (float)(3 * Unskew2D + 2);
				float dy2 = dy0 - (float)(3 * Unskew2D + 1);
				float a2 = Rsquared2D - dx2 * dx2 - dy2 * dy2;
				if (a2 > 0)
				{
					value += (a2 * a2) * (a2 * a2) * Grad(seed, xsbp + (PrimeX << 1), ysbp + PrimeY, dx2, dy2);
				}
			}
			else
			{
				float dx2 = dx0 - (float)Unskew2D;
				float dy2 = dy0 - (float)(Unskew2D + 1);
				float a2 = Rsquared2D - dx2 * dx2 - dy2 * dy2;
				if (a2 > 0)
				{
					value += (a2 * a2) * (a2 * a2) * Grad(seed, xsbp, ysbp + PrimeY, dx2, dy2);
				}
			}

			if (yi - xmyi > 1)
			{
				float dx3 = dx0 - (float)(3 * Unskew2D + 1);
				float dy3 = dy0 - (float)(3 * Unskew2D + 2);
				float a3 = Rsquared2D - dx3 * dx3 - dy3 * dy3;
				if (a3 > 0)
				{
					value += (a3 * a3) * (a3 * a3) * Grad(seed, xsbp + PrimeX, ysbp + (PrimeY << 1), dx3, dy3);
				}
			}
			else
			{
				float dx3 = dx0 - (float)(Unskew2D + 1);
				float dy3 = dy0 - (float)Unskew2D;
				float a3 = Rsquared2D - dx3 * dx3 - dy3 * dy3;
				if (a3 > 0)
				{
					value += (a3 * a3) * (a3 * a3) * Grad(seed, xsbp + PrimeX, ysbp, dx3, dy3);
				}
			}
		}
		else
		{
			if (xi + xmyi < 0)
			{
				float dx2 = dx0 + (float)(1 + Unskew2D);
				float dy2 = dy0 + (float)Unskew2D;
				float a2 = Rsquared2D - dx2 * dx2 - dy2 * dy2;
				if (a2 > 0)
				{
					value += (a2 * a2) * (a2 * a2) * Grad(seed, xsbp - PrimeX, ysbp, dx2, dy2);
				}
			}
			else
			{
				float dx2 = dx0 - (float)(Unskew2D + 1);
				float dy2 = dy0 - (float)Unskew2D;
				float a2 = Rsquared2D - dx2 * dx2 - dy2 * dy2;
				if (a2 > 0)
				{
					value += (a2 * a2) * (a2 * a2) * Grad(seed, xsbp + PrimeX, ysbp, dx2, dy2);
				}
			}

			if (yi < xmyi)
			{
				float dx2 = dx0 + (float)Unskew2D;
				float dy2 = dy0 + (float)(Unskew2D + 1);
				float a2 = Rsquared2D - dx2 * dx2 - dy2 * dy2;
				if (a2 > 0)
				{
					value += (a2 * a2) * (a2 * a2) * Grad(seed, xsbp, ysbp - PrimeY, dx2, dy2);
				}
			}
			else
			{
				float dx2 = dx0 - (float)Unskew2D;
				float dy2 = dy0 - (float)(Unskew2D + 1);
				float a2 = Rsquared2D - dx2 * dx2 - dy2 * dy2;
				if (a2 > 0)
				{
					value += (a2 * a2) * (a2 * a2) * Grad(seed, xsbp, ysbp + PrimeY, dx2, dy2);
				}
			}
		}

		return value;
	}

	/// <summary>
	/// 3D OpenSimplex2S/SuperSimplex noise, with better visual isotropy in (X, Y).
	/// </summary>
	/// <remarks>
	/// Recommended for 3D terrain and time-varied animations.
	/// The Z coordinate should always be the "different" coordinate in whatever your use case is.
	/// If Y is vertical in world coordinates, call Noise3ImproveXZ(x, z, Y) or use Noise3XZBeforeY.
	/// If Z is vertical in world coordinates, call Noise3ImproveXZ(x, y, Z).
	/// For a time varied animation, call Noise3ImproveXY(x, y, T).
	/// </remarks>
	public static float Noise3ImproveXY(long seed, double x, double y, double z)
	{
		// Re-orient the cubic lattices without skewing, so Z points up the main lattice diagonal,
		// and the planes formed by XY are moved far out of alignment with the cube faces.
		// Orthonormal rotation. Not a skew transform.
		double xy = x + y;
		double s2 = xy * Rotate3Orthogonalizer;
		double zz = z * Root3Over3;
		double xr = x + s2 + zz;
		double yr = y + s2 + zz;
		double zr = xy * -Root3Over3 + zz;

		// Evaluate both lattices to form a BCC lattice.
		return Noise3UnrotatedBase(seed, xr, yr, zr);
	}

	/// <summary>
	/// 3D OpenSimplex2S/SuperSimplex noise, with better visual isotropy in (X, Z).
	/// </summary>
	/// <remarks>
	/// Recommended for 3D terrain and time-varied animations.
	/// The Y coordinate should always be the "different" coordinate in whatever your use case is.
	/// If Y is vertical in world coordinates, call Noise3ImproveXZ(x, Y, z).
	/// If Z is vertical in world coordinates, call Noise3ImproveXZ(x, Z, y) or use Noise3ImproveXY.
	/// For a time varied animation, call Noise3ImproveXZ(x, T, y) or use Noise3ImproveXY.
	/// </remarks>
	public static float Noise3ImproveXZ(long seed, double x, double y, double z)
	{
		// Re-orient the cubic lattices without skewing, so Y points up the main lattice diagonal,
		// and the planes formed by XZ are moved far out of alignment with the cube faces.
		// Orthonormal rotation. Not a skew transform.
		double xz = x + z;
		double s2 = xz * -0.211324865405187;
		double yy = y * Root3Over3;
		double xr = x + s2 + yy;
		double zr = z + s2 + yy;
		double yr = xz * -Root3Over3 + yy;

		// Evaluate both lattices to form a BCC lattice.
		return Noise3UnrotatedBase(seed, xr, yr, zr);
	}

	/// <summary>
	/// 3D OpenSimplex2S/SuperSimplex noise, fallback rotation option
	/// </summary>
	/// <remarks>
	/// Use Noise3ImproveXY or Noise3ImproveXZ instead, wherever appropriate.
	/// They have less diagonal bias. This function's best use is as a fallback.
	/// </remarks>
	public static float Noise3Fallback(long seed, double x, double y, double z)
	{
		// Re-orient the cubic lattices via rotation, to produce a familiar look.
		// Orthonormal rotation. Not a skew transform.
		double r = FallbackRotate3 * (x + y + z);
		double xr = r - x,
			yr = r - y,
			zr = r - z;

		// Evaluate both lattices to form a BCC lattice.
		return Noise3UnrotatedBase(seed, xr, yr, zr);
	}

	/// <summary>
	/// Generate overlapping cubic lattices for 3D Re-oriented BCC noise.
	/// </summary>
	/// <remarks
	/// Lookup table implementation inspired by DigitalShadow.
	/// It was actually faster to narrow down the points in the loop itself,
	/// than to build up the index with enough info to isolate 8 points.
	/// </remarks>
	private static float Noise3UnrotatedBase(long seed, double xr, double yr, double zr)
	{
		// Get base points and offsets.
		int xrb = FastFloor(xr),
			yrb = FastFloor(yr),
			zrb = FastFloor(zr);
		float xi = (float)(xr - xrb),
			yi = (float)(yr - yrb),
			zi = (float)(zr - zrb);

		// Prime pre-multiplication for hash. Also flip seed for second lattice copy.
		long xrbp = xrb * PrimeX,
			yrbp = yrb * PrimeY,
			zrbp = zrb * PrimeZ;
		long seed2 = seed ^ -0x52D547B2E96ED629L;

		// -1 if positive, 0 if negative.
		int xNMask = (int)(-0.5f - xi),
			yNMask = (int)(-0.5f - yi),
			zNMask = (int)(-0.5f - zi);

		// First vertex.
		float x0 = xi + xNMask;
		float y0 = yi + yNMask;
		float z0 = zi + zNMask;
		float a0 = Rsquared3D - x0 * x0 - y0 * y0 - z0 * z0;
		float value =
			(a0 * a0)
			* (a0 * a0)
			* Grad(seed, xrbp + (xNMask & PrimeX), yrbp + (yNMask & PrimeY), zrbp + (zNMask & PrimeZ), x0, y0, z0);

		// Second vertex.
		float x1 = xi - 0.5f;
		float y1 = yi - 0.5f;
		float z1 = zi - 0.5f;
		float a1 = Rsquared3D - x1 * x1 - y1 * y1 - z1 * z1;
		value += (a1 * a1) * (a1 * a1) * Grad(seed2, xrbp + PrimeX, yrbp + PrimeY, zrbp + PrimeZ, x1, y1, z1);

		// Shortcuts for building the remaining falloffs.
		// Derived by subtracting the polynomials with the offsets plugged in.
		float xAFlipMask0 = ((xNMask | 1) << 1) * x1;
		float yAFlipMask0 = ((yNMask | 1) << 1) * y1;
		float zAFlipMask0 = ((zNMask | 1) << 1) * z1;
		float xAFlipMask1 = (-2 - (xNMask << 2)) * x1 - 1.0f;
		float yAFlipMask1 = (-2 - (yNMask << 2)) * y1 - 1.0f;
		float zAFlipMask1 = (-2 - (zNMask << 2)) * z1 - 1.0f;

		bool skip5 = false;
		float a2 = xAFlipMask0 + a0;
		if (a2 > 0)
		{
			float x2 = x0 - (xNMask | 1);
			float y2 = y0;
			float z2 = z0;
			value +=
				(a2 * a2)
				* (a2 * a2)
				* Grad(seed, xrbp + (~xNMask & PrimeX), yrbp + (yNMask & PrimeY), zrbp + (zNMask & PrimeZ), x2, y2, z2);
		}
		else
		{
			float a3 = yAFlipMask0 + zAFlipMask0 + a0;
			if (a3 > 0)
			{
				float x3 = x0;
				float y3 = y0 - (yNMask | 1);
				float z3 = z0 - (zNMask | 1);
				value +=
					(a3 * a3)
					* (a3 * a3)
					* Grad(
						seed,
						xrbp + (xNMask & PrimeX),
						yrbp + (~yNMask & PrimeY),
						zrbp + (~zNMask & PrimeZ),
						x3,
						y3,
						z3
					);
			}

			float a4 = xAFlipMask1 + a1;
			if (a4 > 0)
			{
				float x4 = (xNMask | 1) + x1;
				float y4 = y1;
				float z4 = z1;
				value +=
					(a4 * a4)
					* (a4 * a4)
					* Grad(seed2, xrbp + (xNMask & unchecked(PrimeX * 2)), yrbp + PrimeY, zrbp + PrimeZ, x4, y4, z4);
				skip5 = true;
			}
		}

		bool skip9 = false;
		float a6 = yAFlipMask0 + a0;
		if (a6 > 0)
		{
			float x6 = x0;
			float y6 = y0 - (yNMask | 1);
			float z6 = z0;
			value +=
				(a6 * a6)
				* (a6 * a6)
				* Grad(seed, xrbp + (xNMask & PrimeX), yrbp + (~yNMask & PrimeY), zrbp + (zNMask & PrimeZ), x6, y6, z6);
		}
		else
		{
			float a7 = xAFlipMask0 + zAFlipMask0 + a0;
			if (a7 > 0)
			{
				float x7 = x0 - (xNMask | 1);
				float y7 = y0;
				float z7 = z0 - (zNMask | 1);
				value +=
					(a7 * a7)
					* (a7 * a7)
					* Grad(
						seed,
						xrbp + (~xNMask & PrimeX),
						yrbp + (yNMask & PrimeY),
						zrbp + (~zNMask & PrimeZ),
						x7,
						y7,
						z7
					);
			}

			float a8 = yAFlipMask1 + a1;
			if (a8 > 0)
			{
				float x8 = x1;
				float y8 = (yNMask | 1) + y1;
				float z8 = z1;
				value +=
					(a8 * a8)
					* (a8 * a8)
					* Grad(seed2, xrbp + PrimeX, yrbp + (yNMask & (PrimeY << 1)), zrbp + PrimeZ, x8, y8, z8);
				skip9 = true;
			}
		}

		bool skipD = false;
		float aA = zAFlipMask0 + a0;
		if (aA > 0)
		{
			float xA = x0;
			float yA = y0;
			float zA = z0 - (zNMask | 1);
			value +=
				(aA * aA)
				* (aA * aA)
				* Grad(seed, xrbp + (xNMask & PrimeX), yrbp + (yNMask & PrimeY), zrbp + (~zNMask & PrimeZ), xA, yA, zA);
		}
		else
		{
			float aB = xAFlipMask0 + yAFlipMask0 + a0;
			if (aB > 0)
			{
				float xB = x0 - (xNMask | 1);
				float yB = y0 - (yNMask | 1);
				float zB = z0;
				value +=
					(aB * aB)
					* (aB * aB)
					* Grad(
						seed,
						xrbp + (~xNMask & PrimeX),
						yrbp + (~yNMask & PrimeY),
						zrbp + (zNMask & PrimeZ),
						xB,
						yB,
						zB
					);
			}

			float aC = zAFlipMask1 + a1;
			if (aC > 0)
			{
				float xC = x1;
				float yC = y1;
				float zC = (zNMask | 1) + z1;
				value +=
					(aC * aC)
					* (aC * aC)
					* Grad(seed2, xrbp + PrimeX, yrbp + PrimeY, zrbp + (zNMask & (PrimeZ << 1)), xC, yC, zC);
				skipD = true;
			}
		}

		if (!skip5)
		{
			float a5 = yAFlipMask1 + zAFlipMask1 + a1;
			if (a5 > 0)
			{
				float x5 = x1;
				float y5 = (yNMask | 1) + y1;
				float z5 = (zNMask | 1) + z1;
				value +=
					(a5 * a5)
					* (a5 * a5)
					* Grad(
						seed2,
						xrbp + PrimeX,
						yrbp + (yNMask & (PrimeY << 1)),
						zrbp + (zNMask & (PrimeZ << 1)),
						x5,
						y5,
						z5
					);
			}
		}

		if (!skip9)
		{
			float a9 = xAFlipMask1 + zAFlipMask1 + a1;
			if (a9 > 0)
			{
				float x9 = (xNMask | 1) + x1;
				float y9 = y1;
				float z9 = (zNMask | 1) + z1;
				value +=
					(a9 * a9)
					* (a9 * a9)
					* Grad(
						seed2,
						xrbp + (xNMask & unchecked(PrimeX * 2)),
						yrbp + PrimeY,
						zrbp + (zNMask & (PrimeZ << 1)),
						x9,
						y9,
						z9
					);
			}
		}

		if (!skipD)
		{
			float aD = xAFlipMask1 + yAFlipMask1 + a1;
			if (aD > 0)
			{
				float xD = (xNMask | 1) + x1;
				float yD = (yNMask | 1) + y1;
				float zD = z1;
				value +=
					(aD * aD)
					* (aD * aD)
					* Grad(
						seed2,
						xrbp + (xNMask & (PrimeX << 1)),
						yrbp + (yNMask & (PrimeY << 1)),
						zrbp + PrimeZ,
						xD,
						yD,
						zD
					);
			}
		}

		return value;
	}

	/// <summary>
	/// 4D SuperSimplex noise, with XYZ oriented like Noise3ImproveXY
	/// and W for an extra degree of freedom. W repeats eventually.
	/// </summary>
	/// <remarks
	/// Recommended for time-varied animations which texture a 3D object (W=time)
	/// in a space where Z is vertical
	/// </remarks>
	public static float Noise4ImproveXYZImproveXY(long seed, double x, double y, double z, double w)
	{
		double xy = x + y;
		double s2 = xy * -0.21132486540518699998;
		double zz = z * 0.28867513459481294226;
		double ww = w * 1.118033988749894;
		double xr = x + (zz + ww + s2),
			yr = y + (zz + ww + s2);
		double zr = xy * -0.57735026918962599998 + (zz + ww);
		double wr = z * -0.866025403784439 + ww;

		return Noise4_UnskewedBase(seed, xr, yr, zr, wr);
	}

	/// <summary>
	/// 4D SuperSimplex noise, with XYZ oriented like Noise3ImproveXZ
	/// and W for an extra degree of freedom. W repeats eventually.
	/// </summary>
	/// <remarks
	/// Recommended for time-varied animations which texture a 3D object (W=time)
	/// in a space where Y is vertical
	/// </remarks>
	public static float Noise4_ImproveXYZ_ImproveXZ(long seed, double x, double y, double z, double w)
	{
		double xz = x + z;
		double s2 = xz * -0.21132486540518699998;
		double yy = y * 0.28867513459481294226;
		double ww = w * 1.118033988749894;
		double xr = x + (yy + ww + s2),
			zr = z + (yy + ww + s2);
		double yr = xz * -0.57735026918962599998 + (yy + ww);
		double wr = y * -0.866025403784439 + ww;

		return Noise4_UnskewedBase(seed, xr, yr, zr, wr);
	}

	/// <summary>
	/// 4D SuperSimplex noise, with XYZ oriented like Noise3Fallback
	/// and W for an extra degree of freedom. W repeats eventually.
	/// </summary>
	/// <remarks
	/// Recommended for time-varied animations which texture a 3D object (W=time)
	/// where there isn't a clear distinction between horizontal and vertical
	/// </remarks>
	public static float Noise4_ImproveXYZ(long seed, double x, double y, double z, double w)
	{
		double xyz = x + y + z;
		double ww = w * 1.118033988749894;
		double s2 = xyz * -0.16666666666666666 + ww;
		double xs = x + s2,
			ys = y + s2,
			zs = z + s2,
			ws = -0.5 * xyz + ww;

		return Noise4_UnskewedBase(seed, xs, ys, zs, ws);
	}

	/// <summary>
	/// 4D SuperSimplex noise, fallback lattice orientation.
	/// </summary>
	public static float Noise4_Fallback(long seed, double x, double y, double z, double w)
	{
		// Get points for A4 lattice
		double s = Skew4D * (x + y + z + w);
		double xs = x + s,
			ys = y + s,
			zs = z + s,
			ws = w + s;

		return Noise4_UnskewedBase(seed, xs, ys, zs, ws);
	}

	/// <summary>
	/// 4D SuperSimplex noise base.
	/// </summary>
	/// <remarks>
	/// Using ultra-simple 4x4x4x4 lookup partitioning.
	/// This isn't as elegant or SIMD/GPU/etc. portable as other approaches,
	/// but it competes performance-wise with optimized 2014 OpenSimplex.
	/// </remarks>
	private static float Noise4_UnskewedBase(long seed, double xs, double ys, double zs, double ws)
	{
		// Get base points and offsets
		int xsb = FastFloor(xs),
			ysb = FastFloor(ys),
			zsb = FastFloor(zs),
			wsb = FastFloor(ws);
		float xsi = (float)(xs - xsb),
			ysi = (float)(ys - ysb),
			zsi = (float)(zs - zsb),
			wsi = (float)(ws - wsb);

		// Unskewed offsets
		float ssi = (xsi + ysi + zsi + wsi) * Unskew4D;
		float xi = xsi + ssi,
			yi = ysi + ssi,
			zi = zsi + ssi,
			wi = wsi + ssi;

		// Prime pre-multiplication for hash.
		long xsvp = xsb * PrimeX,
			ysvp = ysb * PrimeY,
			zsvp = zsb * PrimeZ,
			wsvp = wsb * PrimeW;

		// Index into initial table.
		int index =
			((FastFloor(xs * 4) & 3) << 0)
			| ((FastFloor(ys * 4) & 3) << 2)
			| ((FastFloor(zs * 4) & 3) << 4)
			| ((FastFloor(ws * 4) & 3) << 6);

		// Point contributions
		float value = 0;
		(int secondaryIndexStart, int secondaryIndexStop) = Lookup4DA[index];
		for (int i = secondaryIndexStart; i < secondaryIndexStop; i++)
		{
			LatticeVertex4D c = Lookup4DB[i];
			float dx = xi + c.Dx,
				dy = yi + c.Dy,
				dz = zi + c.Dz,
				dw = wi + c.Dw;
			float a = (dx * dx + dy * dy) + (dz * dz + dw * dw);
			if (a < Rsquared4D)
			{
				a -= Rsquared4D;
				a *= a;
				value += a * a * Grad(seed, xsvp + c.Xsvp, ysvp + c.Ysvp, zsvp + c.Zsvp, wsvp + c.Wsvp, dx, dy, dz, dw);
			}
		}
		return value;
	}

